export const getAreaName = (key) => {
  if (key.startsWith('area_control')) {
    return 'Area Control'
  } else if (key.startsWith('area_cebo')) {
    return 'Area Cebo'
  } else if (key.startsWith('area_hallazgos')) {
    return 'Area Hallazgos'
  }
  return 'Otro' // Si hay claves no identificadas
}
